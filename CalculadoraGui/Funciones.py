import math

def suma(a,b):
    r = a + b
    return r
#////////////////////////////////////////////////////////////////////////
def resta(a,b):
    r = a - b
    return r
#/////////////////////////////////////////////////////////////////////////////
def multi(a,b):
    r = a * b
    return r
#//////////////////////////////////////////////////////////////////////////////////
def divi(a,b):
    r = a / b
    return r
#////////////////////////////////////////////////////////////////////////////////////////
def seno(a):
    b = math.radians(a)
    resultado = math.sin(b)
    return resultado
#////////////////////////////////////////////////////////////////////////////////////////
def coseno(a):
    b = math.radians(a)
    resultado = math.cos(b)
    return resultado
#//////////////////////////////////////////////////////////////////////////////////////////////
def tangente(a):
    b = math.radians(a)
    resultado = math.tan(b)
    return resultado
#///////////////////////////////////////////////////////////////////////////////////////////
def senoInver(a):
    resultado = math.asin(a)
    resul = math.degrees(resultado)
    return resul
#/////////////////////////////////////////////////////////////////////////////////////////////
def coseInver(a):
    resultado = math.acos(a)
    resul = math.degrees(resultado)
    return resul
#/////////////////////////////////////////////////////////////////////////////////////
def tanInver(a):
    resultado = math.atan(a)
    resul = math.degrees(resultado)
    return resul
